let numero1=8;
let numero2=6;

// Operadores numericos
// Suma
let suma=numero1+numero2;
console.log("************* SUMAS *******************");
console.log(`El resultado de ${numero1}+${numero2} es igual a ${suma}`);

console.log(`El resultado de ${numero1}+${numero2} es igual a ${numero1+numero2}`);

// Resta
let resta=numero1-numero2;
console.log("************* RESTAS *******************");
console.log(`El resultado de ${numero1}-${numero2} es igual a ${resta}`);

console.log(`El resultado de ${numero1}-${numero2} es igual a ${numero1-numero2}`);

// Multiplicacion
let multiplicacion=numero1*numero2;
console.log("************* MULTIPLICACIONES *******************");
console.log(`El resultado de ${numero1}*${numero2} es igual a ${multiplicacion}`);

console.log(`El resultado de ${numero1}*${numero2} es igual a ${numero1*numero2}`);

// Division
let division=numero1/numero2;
console.log("************* DIVISIONES *******************");
console.log(`El resultado de ${numero1}/${numero2} es igual a ${division}`);

console.log(`El resultado de ${numero1}/${numero2} es igual a ${numero1/numero2}`);

// Modulo => RESTO DE LA DIVISIÃ“N ENTERA
let modulo=numero1%numero2;
console.log("************* MODULO *******************");
console.log(`El resultado de ${numero1}%${numero2} es igual a ${modulo}`);

console.log(`El resultado de ${numero1}%${numero2} es igual a ${numero1%numero2}`);

// Potencia
let potencia=numero1**3;
console.log("************* POTENCIA *******************");
console.log(`El resultado de ${numero1} elevado 3 es igual a ${potencia}`);
console.log(`El resultado de ${numero1} elevado 3 es igual a ${numero1**3}`);


// Operadores especiales
// numero1+=2 => numero1=numero1+2
numero1=numero1+numero1;
numero1*=2;
console.log(`Numero1 = ${numero1}`);

// numero1++ es igual numero1=numero1+1
numero1++;
console.log(`Numero1 = ${numero1}`);