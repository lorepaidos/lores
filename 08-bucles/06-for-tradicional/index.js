// El for consta de tres partes
// for(puntoInicial;condicion;incremento)
// PuntoInicial: numero en el cual empezamos
// Condicion: condicion durante la cual se ejecuta
// el bucle
// Incremento: incremento del contador

for(let i=0;i<10;i++){
    console.log(i);
}

// Es igual a 
i = 0;
while (i<10) {
    console.log(i);
    i++;
}